/** Build the font strip texture and register it as a RetroFont. */
export function buildFont(scene: Phaser.Scene): void {
  if (scene.cache.bitmapFont.exists(FONT_KEY)) return;
  const cell = 8;
  const w = FONT_CHARS.length * cell;
  const tex = scene.textures.createCanvas(FONT_IMAGE_KEY, w, cell);
  if (!tex) return;
  const ctx = tex.getContext();
  ctx.fillStyle = '#ffffff';
  for (let i = 0; i < FONT_CHARS.length; i++) {
    const cols = GLYPHS[FONT_CHARS[i]] ?? GLYPHS[' '];
    for (let c = 0; c < 5; c++) {
      for (let r = 0; r < 7; r++) {
        if (cols[c] & (1 << r)) {
          ctx.fillRect(i * cell + c + 1, r, 1, 1);
        }
      }
    }
  }
  tex.refresh();

  const config: Phaser.Types.GameObjects.BitmapText.RetroFontConfig = {
    image: FONT_IMAGE_KEY,
    width: cell,
    height: cell,
    chars: FONT_CHARS,
    charsPerRow: FONT_CHARS.length,
    'spacing.x': 0,
    'spacing.y': 0,
    'offset.x': 0,
    'offset.y': 0,
    lineSpacing: 2
  };
  scene.cache.bitmapFont.add(
    FONT_KEY,
    Phaser.GameObjects.RetroFont.Parse(scene, config)
  );
}
