/**
 * Horizontally concatenate chunks into one 15-row level grid.
 * Rows shorter than the widest row of their chunk are padded with '.'.
 */
export function assembleRows(chunkNames: string[]): string[] {
  const rows: string[] = new Array(15).fill('');
  for (const name of chunkNames) {
    const chunk = CHUNKS[name];
    if (!chunk) throw new Error(`Unknown chunk: ${name}`);
    if (chunk.length !== 15) throw new Error(`Chunk ${name} must have 15 rows, has ${chunk.length}`);
    const width = Math.max(...chunk.map((r) => r.length));
    for (let i = 0; i < 15; i++) {
      rows[i] += chunk[i].padEnd(width, '.');
    }
  }
  return rows;
}
